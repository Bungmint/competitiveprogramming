void solve()
{
    ll x, y;
    cin >> x >> y;
    ll ans = 0;
    for (ll i = 1; i <= sqrt(x); i++)
    {
        ans += max(0LL, min(x / i - 1, y) - i);
    }
    cout << ans << "\n";
}

