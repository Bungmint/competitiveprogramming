ar<int,3> dp[501][501];
int n, a[501];

ar<int,3> dping(int l, int r){
	ar<int,3>&res = dp[l][r];
	if (res[0]!=-1) return res;
	if (r==l) return res = {a[l], 0, 0};
	res = {0,0, 0};
	for (int i=l;i<r;++i){
		ar<int,3> p = dping(l, i), q=dping(i+1, r);
		if (p[0]&&q[0]&&p[0]==q[0]){
			res[0] = p[0]+1;
			res[1] = p[1]+q[1]+1;
			res[2] = res[1];
		}else{
			res[2] = max(res[2], p[2]+q[2]);
		}
		dbg(p, q);
	}
	dbg(l, r, res);
	return res;
}

