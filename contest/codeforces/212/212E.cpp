const int N = 5001;
Mint dp[N][N];
pi road[N];
set<pi> st;

void solve()
{
	int n, m, k;
	cin >> n>>m >>k;
	for (int i=0;i<m;++i){
		int u, v;
		cin >> u >> v;
		road[i] = {u,v};
	}
	dp[1][0] = 1;
	for (int day=1;day<=k;day++){
		Mint total = 0;
		for (int v=1;v<=n;v++){
			total += dp[v][day-1];
		}
		for (int v= 1;v<=n;v++){
			dp[v][day] = total-dp[v][day-1];
		}
		for (int i=0;i<m;++i){
			auto [u,v] = road[i];
			dp[v][day] -=dp[u][day-1];
			dp[u][day] -= dp[v][day-1];
		}
	}
	cout << dp[1][k] << endl;
}

