#undef all
using ull = unsigned long long;
using pull = pair<ull, ull>;

void solve()
{
	int n, m;
	cin >> n >> m;
	vector<vpi> adj(n);
	for (int i : rep(m)) {
		int x, y;
		cin >> x >> y, x--, y--;
		adj[x].pb({y, i});
		adj[y].pb({x, i});
	}
	auto setI = [&](pull& p, int i) -> void {
		if (i >= 64) p.se |= (1LL << (i - 64));
		else p.fi |= (1LL << i);
	};
	auto isSet = [&](pull& p, int i) -> bool {
		if (i >= 64) return (p.se >> (ll)(i - 64)) & 1;
		return (p.fi >> (ll)i) & 1;
	};
	set<pull> hashes;
	for (int mask : rep(1 << n)) {
		pull orange{};
		bitset<16> vis{};
		for (int i : rep(n)) {
			if ((mask & (1 << i))) continue;
			for (auto &[j, id] : adj[i]) {
				if (mask & (1 << j)) setI(orange, id);
			}
		}
		auto dfs = y_combinator([&](auto self, int u) -> void {
			vis[u] = 1;
			for (auto &[j, id] : adj[u]) {
				if (!vis[j] && isSet(orange, id)) self(j);
			}
		});
		dfs(0);
		dbg(vis, orange);
		if (vis.count() == n) hashes.insert(orange);
	}
	cout << sz(hashes) << '\n';
}

