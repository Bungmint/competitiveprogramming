void solve()
{
	ll a1, a2, a3;
	cin >> a1 >> a2 >> a3;
	if (a3<a1) swap(a1, a3);
	if (a1<=a2&&a2<=a3){
		ll x = a2-a1, y = a3-a2;
		dbg(x,y);
		if (x<=y){
			if ((x+y)&1) cout << (x+y+1)/2LL -x + 1;
			else cout << (x+y)/2LL - x;
		}else{
			cout << x-y;
		}
	}else if (a1<=a2&&a2>=a3){
		ll x = a2-a1, y = a2-a3;
		cout << x+y;
	}else if (a1>=a2&&a2<=a3){
		ll x = a1-a2, y = a3-a2;
		if ((a3-a1)&1) cout << x + (a3-a1+1)/2LL +1;
		else cout << (a3-a1)/2LL + x;
	}
}

